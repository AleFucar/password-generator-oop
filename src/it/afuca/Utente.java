package it.afuca;

public class Utente {

	static String name;
	static String surname;
	static String color;
	static int day;
	static int month;
	static int year;
	

	public Utente(String name, String surname, String color, int day, int month, int year) {

		this.name = name;
		this.surname = surname;
		this.color = color;
		this.day = day;
		this.month = month;
		this.year = year;
		
	}

}
